func (t *TimezonesConfig) Map(in string) (*Timezone, error) {
	config, exists := t.timezones[in]
	if !exists {
		return nil, fmt.Errorf("%s", "unkown timezone: "+in)
	}
	return config, nil
}

func (t *TimezonesConfig) GetByParameters(offset string, std string, dst string) *string {
	for name, timezone := range t.timezones {
		if timezone.Offset == offset {
			if timezone.TimeStd == std {
				if timezone.TimeDst == dst {
					return &name
				}
			}
		}
	}

	return nil
}
